"""
Logger place holder
"""

def info(msg):
	"""Summary
	
	Args:
	    msg (TYPE): Description
	"""
	print(msg)

def debug(msg):
	"""Summary
	
	Args:
	    msg (TYPE): Description
	"""
	print(msg)

def warning(msg):
	"""Summary
	
	Args:
	    msg (TYPE): Description
	"""
	print(msg)

def error(msg):
	"""Summary
	
	Args:
	    msg (TYPE): Description
	"""
	print(msg)
	raise Exception(msg)